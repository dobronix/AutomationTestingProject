package ObjectData;



public class ContactFormObjectData {
    private String name;
    private String email;
    private String subject;
    private String yourmessage;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getSubject() {
        return subject;
    }

    public String getYourmessage() {
        return yourmessage;
    }
}
